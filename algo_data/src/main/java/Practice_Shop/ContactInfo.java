package Practice_Shop;

public class ContactInfo {
    private String email;
    private String cell;

    public void setEmail(String email) {
        this.email = email;
    }

    public void setCell(String cell) {
        this.cell = cell;
    }

    public String getEmail() {
        return email;
    }

    public String getCell() {
        return cell;
    }
}
